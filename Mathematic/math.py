def powNumber(num1,num2): 
    return num1 ** num2

def addNumber(num1,num2):
    return num1 + num2

def subNumber(num1,num2):
    return num1 - num2

def mulNumber(num1,num2):
    return num1 * num2

def divNumber(num1,num2):
    return num1 / num2